void animateBitmapAppearFade(const uint8_t *bitmap, uint8_t bmp_width, uint8_t bmp_height, uint16_t holdTime) {
  int x = (SCREEN_WIDTH - bmp_width) / 2;
  int y_center = (SCREEN_HEIGHT - bmp_height) / 2;

  for (int y = SCREEN_HEIGHT; y >= y_center; y--) {
    display.clearDisplay();
    display.drawBitmap(x, y, bitmap, bmp_width, bmp_height, SSD1306_WHITE);
    display.display();
    delay(5);
  }

  delay(holdTime);
  display.clearDisplay();
}
