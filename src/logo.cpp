void animateBitmapAppearFade(const uint8_t *bitmap, uint8_t bmp_width, uint8_t bmp_height, uint16_t holdTime) {
  int x = (SCREEN_WIDTH - bmp_width) / 2;
  int y_center = (SCREEN_HEIGHT - bmp_height) / 2;

  for (int y = SCREEN_HEIGHT; y >= y_center; y--) {
    display.clearDisplay();
    display.drawBitmap(x, y, bitmap, bmp_width, bmp_height, SSD1306_WHITE);
    display.display();
    delay(5);
  }

  delay(holdTime);
  display.clearDisplay();
}

void loadingAnimation() {
  const int barWidth = SCREEN_WIDTH - 20;
  const int barHeight = 10;
  const int barX = 10;
  const int barY = (SCREEN_HEIGHT - barHeight) / 2;
  const float duration = 1.0;
  const int steps = 100;
  const float delayTime = (duration / steps) * 1000;

  for (int i = 0; i <= steps; i++) {
    display.clearDisplay();
    display.setTextSize(1);
    display.setTextColor(SSD1306_WHITE);
    display.setCursor(0, 0);
	display.println("Made by - zern404");
    display.println("Loading...");

    int progress = (barWidth * i) / steps;
    display.drawRect(barX, barY, barWidth, barHeight, SSD1306_WHITE);
    display.fillRect(barX, barY, progress, barHeight, SSD1306_WHITE);

    display.setCursor(SCREEN_WIDTH / 2 - 10, barY + barHeight + 5);
    display.print(i);
    display.print("%");

    display.display();
    delay(delayTime);
  }

  for (int j = 0; j < 3; j++) {
    display.clearDisplay();
    display.setTextSize(1);
    display.setTextColor(SSD1306_WHITE);
    display.setCursor(0, 0);
	display.println("Made by - zern404");
    display.println("Loading...");

    display.drawRect(barX, barY, barWidth, barHeight, SSD1306_WHITE);
    display.fillRect(barX, barY, barWidth, barHeight, SSD1306_WHITE);

    display.setCursor(SCREEN_WIDTH / 2 - 10, barY + barHeight + 5);
    display.print("100%");
    display.display();
    delay(150);

    display.clearDisplay();
    display.display();
    delay(200);
  }

  display.clearDisplay();
  display.display();
}